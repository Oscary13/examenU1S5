    <p>
        Una comunicación orientada a la conexión es un modo de comunicación de datos que requiere una sobrecarga al
        configurar una solicitud de conexión antes de enviar cualquier tipo de mensaje de comunicación de datos. El modo
        orientado a la conexión (modo CO), diferenciado de la comunicación sin conexión, es requerido por la mayoría de los
        protocolos de red de computadoras para garantizar que no se pierdan paquetes. Los protocolos son un conjunto de
        estándares y reglas que admiten modos específicos de comunicación de datos. La comunicación orientada a la conexión
        se diseñó para garantizar que haya recursos disponibles según lo requiera el tipo de comunicaciones que se
        solicitan.
