@section('subtitulo1')
    <h2>¿Qué es un sistema de información?</h2>

@endsection
@section('parrafoSub1')
    <p>
        Cuando se habla de un sistema de información (SI) se refiere a un conjunto ordenado de mecanismos que
        tienen como fin la administración de datos y de información, de manera que puedan ser recuperados y
        procesados fácil y rápidamente.
        Todo sistema de información se compone de una serie de recursos interconectados y en interacción,
        dispuestos del modo más conveniente en base al propósito informativo trazado, como puede ser recabar
        información personal, procesar estadísticas, organizar archivos, etc. 
    </p>
    <p>
        
        <strong>Estos recursos pueden ser:</strong>
    </p>
@endsection
@section('imagen2')
    <span class="image main"><img src="images/100.png" alt="" /></span>
@endsection
@section('parrafo2')
    <p> • <strong>Los sistemas de control de calidad:</strong> En los que se pide una retroalimentación al cliente y se
        evalúan los resultados estadísticamente para elaborar resultados interpretables por la gerencia.
    </p>
    <p>
        • <strong>Las bases de datos de una biblioteca: </strong> En donde está contenido el grueso volumen de documentos
        (libros, revistas, tesis, etc.) de la biblioteca, en función de ubicar y recuperar cada uno lo más rápida y
        precisamente posible.
    </p>
    <p>
        • <strong>Las hojas de cálculo:</strong> En las que se ingresa información en bruto y se la organiza de manera
        cuantificable para obtener directrices de conducción financiera.
    </p>

    <p>
        Si bien los Sistemas de Información pueden diferir en la forma en que se usan dentro de una organización, todos
        tienen los siguientes componentes: <br><br>

        <strong>- Hardware: </strong> los sistemas utilizan hardware local como una computadora o servicios de Nube para su ejecución. <br>
        <strong>- Software:</strong> estos son los programas utilizados para administración, procesamiento y análisis. <br>
        <strong>- Bases de datos:</strong> los sistemas trabajan con recursos organizados en tablas y archivos. <br>
        <strong>- Red:</strong> se deben conectar diferentes recursos entre sí, especialmente si muchas personas diferentes en una organización
          usan el mismo sistema. <br>
        <strong>- Procedimientos:</strong> describen cómo se procesan y analizan los datos y recursos específicos para obtener las respuestas
          para las que está diseñado el sistema (“lógica del negocio”). <br>
          <br><br>
    </p>
@endsection

