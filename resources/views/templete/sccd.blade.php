    <p>
        <strong>Computadoras dedicadas:</strong><br>
        Esta es la era en la que cada servicio empleaba su propia computadora que permitía que los usuarios de ese servicio
        se conectaran directamente. Esto es consecuencia de la aparición de computadoras pequeñas, de fácil uso, más baratas
        y poderosas de las convencionales. <br>
        Un servidor dedicado es un equipo informático físico que destina todos sus recursos a proporcionar información y
        atender las peticiones de otro ordenador (cliente) que ha contratado sus servicios. Por tanto, a diferencia de un
        servidor compartido, el dedicado trabaja en exclusiva para un solo cliente. <br><br>

        <strong>Características de un servidor dedicado:</strong> <br>
        Un servidor dedicado se caracteriza, esencialmente, por ofrecer sus recursos y servicios a un único individuo,
        aunque también son importantes los siguientes aspectos:
    <ul>
        <li>Se localiza, por regla general, en un centro de datos, lugar en el que se encuentran todos los recursos que una
            entidad necesita para procesar su información.</li>
        <li>Proporciona más seguridad y mayor velocidad, ya que los recursos de este gran ordenador central no se comparten
            con otros usuarios.</li>
        <li>Está destinado a alojar sitios web con un elevado tráfico, siendo recomendable por tanto que las que contratan
            estos servicios reciban un gran número de visitas diarias.</li>
    </ul>

    <strong>Ventajas e inconvenientes de un servidor dedicado:</strong><br>
    La ventaja más notable de un servidor dedicado es que un único cliente puede disfrutar y hacer uso de todos los recursos
    de la máquina, la cual, además, es capaz de adaptarse por completo a las necesidades del cliente por ser libremente
    configurable. <br>
    Por otro lado, el principal inconveniente de un servidor dedicado no es otro que su coste, situado muy por encima del
    precio de un alojamiento compartido.

    </p>
