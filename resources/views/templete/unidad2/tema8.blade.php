    <p>
        <strong>IAAS:</strong><br>
        IaaS (Infraestructura como servicio) es un modelo de entrega de servicios en la nube en el que las empresas cuentan
        con todos los recursos necesarios para el procesamiento de energía, redes, almacenamiento, redes y otros servicios.
        <br><br>

        En el modelo IaaS, las empresas compran o alquilan recursos de cómputo altamente escalables y automatizados a pedido
        y según sea necesario, lo que les permite canjear los gastos de capital por gastos variables. Pagan sobre la marcha
        y solo por lo que usan. <br><br>

        Infraestructura como servicio permite a las organizaciones de TI administrar sus aplicaciones de manera flexible.
        Obtienen acceso a sus servidores y almacenamiento a través de un panel o API. Básicamente, pueden construir su
        propio "centro de datos virtual" en la nube. <br><br>


        IaaS es la capa de entrega de servicios en la nube más flexible y personalizable; Ofrece un autoservicio completo
        para administrar aplicaciones, tiempo de actividad, middleware y datos. <br><br>
    </p>
    <p>
        <strong>PAAS:</strong><br>

        PaaS (Platform as a Service) es un modelo de prestación de servicios en la nube, que permite a los desarrolladores y
        profesionales operativos crear software sobre las plataformas proporcionadas. <br><br>

        PaaS les brinda a los desarrolladores una variedad de formas de desarrollar, personalizar, administrar y dar soporte
        a su software y aplicaciones. <br><br>

        Sin embargo, Platform as a Service es la capa de entrega de servicios en la nube más difícil de entender. Actúa como
        un híbrido de SaaS y IaaS (ver más abajo). <br><br>

        En el modelo de PaaS, los desarrolladores no tienen que aprovisionar la infraestructura, sino obtener un entorno en
        el que puedan desarrollar y escalar sus aplicaciones de manera flexible. <br><br>

        También, proporciona a los desarrolladores muchas herramientas de desarrollo, prueba e implementación, y les permite
        colaborar en un software en desarrollo. Debido a la virtualización, se garantiza una alta escalabilidad. <br><br>

    </p>
    <p>
        <strong>SAAS:</strong><br>

        SaaS (software como servicio) es un modelo de prestación de servicios en la nube en el que un proveedor de nube
        externo aloja una parte del software y el cliente puede acceder a él fácilmente a través de la web. <br><br>

        En el modelo SaaS, el navegador web del cliente actúa como el punto de acceso al software y las aplicaciones que se
        ejecutan en servidores de terceros (es decir, infraestructura). El software es implementado, administrado,
        actualizado y soportado por un tercero también. <br><br>

        El software SaaS está empaquetado; no es muy personalizable y se ve igual para todos los clientes. Para comenzar a
        usar una solución SaaS, generalmente necesita iniciar sesión y suscribirse. <br><br>

    </p>
