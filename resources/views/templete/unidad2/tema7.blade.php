        <strong>Nube privada:</strong> <br>
        El modelo de nube privada hace referencia a los recursos de computación en nube que se usan exclusivamente en una
        misma empresa u organización. La nube privada puede almacenarse en el centro de datos interno o un proveedor de
        servicios puede alojarla en la nube.
    </p>

    <p>
        <strong>Nube pública:</strong> <br>
        Cuando una organización implementa un modelo de nube pública, toda la infraestructura de hardware, software y
        soporte es propiedad del proveedor de servicios que la administra y la proporciona exclusivamente por Internet.
        Puede acceder a estos servicios y administrar la cuenta mediante un navegador web.
    </p>
    <p>
        <strong>Nube híbrida:</strong> <br>
        Un modelo de computación en nube híbrido combina las nubes públicas y privadas para compartir datos y aplicaciones.
        Las nubes híbridas conectan la infraestructura y las aplicaciones entre recursos en nube con los recursos existentes
        que no se encuentran en la nube. Este enfoque le aporta a su empresa más flexibilidad y más opciones de
        implementación.
