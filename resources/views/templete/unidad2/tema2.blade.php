        hacer en una sola fase, porque necesitamos seguir utilizando las instalaciones y aulas del centro. En este caso el
        plan requiere de una planificación más exhaustiva, de manera que podamos organizar las diferentes fases del proyecto
        sin que afecte al uso educativo.
