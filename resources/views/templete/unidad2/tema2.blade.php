    <p>
        <strong>En general, nos encontramos con dos tipos de planes maestros:</strong> <br /> <br />
        <strong>1.- </strong> Aquellos en los que nuestro objetivo es diseñar un nuevo centro, partiendo de cero y en cuyo
        caso podemos
        construirlo entero de una vez, o se planifica en diferentes fases, determinando en cada una de ellas lo que se
        construye (infantil, primaria, comedor…) y sus sucesivas ampliaciones: secundaria, bachillerato, formación
        profesional.

    </p>
