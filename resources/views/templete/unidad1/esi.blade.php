@section('parrafoSub1')
    <p>
        La recolección y análisis de datos es tan antigua como la civilización misma. Sin embargo, los sistemas de
        información
        como los conocemos en la informática tienen su origen en la década de los 50’.
    </p><br>
    <p>
        <strong>1950-1960</strong><br>
        No es secreto para nadie que a lo largo de la década de los 50’, los sistemas de información eran bastantes básicas
        y
        primitivos, y esto se debía a la tecnología de la era. <br>
        Por esto la primordial funcionalidad de estos sistemas era asistir a los trabajadores de diferentes zonas
        ayudándoles en
        sus diversas labores, siendo varias de estas el procesar transacciones, conservar los registros y llevar la
        contabilidad
        elemental. Así, el SI (Sistema de información), se usaron primordialmente en el procesamiento electrónico de datos.
        A esto se le puede llamar “sistemas de información de base de datos”.
    </p><br>
    <p>
        <strong>1960-1970</strong><br>
        surgieron los sistemas de información gerencial, además conocidos como MSI, los cuales presentaron la novedad de
        transformar todos los datos almacenados en información eficaz, lo cual ayudó en la toma de elecciones empresariales.
        Los
        MSI presentaban su información a modo de pantalla e informes pre-especificados, los cuales respaldaban la toma de
        elecciones empresariales. <br>
        De los ejemplos de MSI, podemos mencionar a los “Sistemas de informes de tendencias de costos”, los “análisis de
        ventas”
        y el “rendimiento de producción”.
    </p><br>
    <p>
        <strong>1970-1980</strong><br>
        La década de los 70’ es sin duda una de las más importantes en la historia de los ordenadores.
        Debido a que ha sido ahí donde se produjo un desarrollo fundamental con la llegada de las pcs particulares, lo que
        facilitó la extensión de los sistemas informáticos, o en otros términos: Los sistemas de información basados en pcs.
        El
        nuevo papel que tomó los SI para lograr conceder soporte ad-hoc interactivo que aporta en el proceso de la toma de
        elecciones, es conocido además con las siglas DSS (Sistemas de Soporte de Decisiones).
        Este contribuye para la gestión y planificación de una organización
    </p><br>
    <p>
    <strong>1980-1990</strong><br>
    Durante la década de los 80’ se desarrolló la informática departamental. Esto se dio gracias a que muchas empresas y
    organizaciones compraban su propio software y hardware con el fin de satisfacer sus necesidades departamentales
    Por otro lado, cabe mencionar que los altos ejecutivos de entonces no utilizaban DSS ni MSI, por lo que se desarrollaron
    los “Sistemas de Información Ejecutivo” conocido con las siglas “ESI”. <br>
    Los ESI suministran a los ejecutivos información interna y externa relevante, las cuales contribuyen a cumplir los
    objetivos estratégicos de la organización, ofreciendo servicios de toma de decisiones.
</p><br>
<p>
    <strong>1990-2000</strong><br>
    Durante ese periodo el mundo presenció la aparición de ERP (sistemas de planificación de recursos empresariales). Los
    ERP incorporan todo los componentes de una organización: Marketing, fabricación, ventas, gestión de recursos y
    planificación de recursos humanos. <br>
    Estos avances se pudieron ver en el desarrollo de la IA (Inteligencia artificial), en el ES (Sistemas expertos) y los
    KMS (Sistemas de gestión del conocimiento). <br>
    Los ES son un sistema que imita la capacidad de las tomas de decisiones humanas. Y los KMS es un sistema que almacena y
    recupera conocimiento, para apoyar la creación, gestión y divulgación del conocimiento empresarial dentro de la
    organización.
</p><br>
<p>
    <strong>2000-presente</strong><br>
    Una era crucial en el desarrollo tecnológico, y por consiguiente en el informativo. A partir del 2000 se comenzaron a
    gestar nuevos softwares, hardware y nuevas formas de negocio que derivaron de las mejoras tecnológicas. <br>
    El internet y la tecnología cambiaron la forma en que las empresas y las personas trabajan. Sin embargo, las funciones
    de los Sistemas de información de esta década siguen siendo las mismas que hace 40 o 50 años. Siguen realizando
    mantenimiento de registros, gestión de informes, procesamiento de transacciones, gestión de organización y gestión de
    soporte. <br>
    Todo lo mencionado se emplea como una forma de respaldar el proceso comercial, la toma de decisiones y la ventaja en
    términos de competencia.
</p><br>
